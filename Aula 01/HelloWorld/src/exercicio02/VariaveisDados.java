package exercicio02;

class VariaveisETiposDeDados {
    public static void main(String[] args) {
        int numeroInteiro = 5;
        double numeroDecimal = 10.7;
        boolean valorBooleano = true;
        char caractere = 'L';

        System.out.println("Inteiro: " + numeroInteiro);
        System.out.println("Double: " + numeroDecimal);
        System.out.println("Booleano: " + valorBooleano);
        System.out.println("Char: " + caractere);
    }
}

